def load_challenges() -> list[dict]:
    wb = openpyxl.load_workbook(XLSX, read_only=True)
    ws = wb["全課題一覧"]
    challenges = []
    for r in range(2, ws.max_row + 1):
        row = {
            "industry_no": ws.cell(r, 1).value,
            "industry": ws.cell(r, 2).value,
            "challenge_no": ws.cell(r, 3).value,
            "challenge": ws.cell(r, 4).value,
            "pain": ws.cell(r, 5).value or "",
            "dept": ws.cell(r, 6).value or "",
        }
        if row["challenge"]:
            challenges.append(row)
    wb.close()
    return challenges


def main():
    challenges = load_challenges()
    print(f"Loaded {len(challenges)} challenges")

    profiles = {}
    for ch in challenges:
        key = f"{ch['industry_no']}-{ch['challenge_no']}"
        ftype = classify(ch["challenge"], ch["pain"])
        profile = _make_profile_from_challenge(ch, ftype)
        profiles[key] = profile

    OUT.parent.mkdir(parents=True, exist_ok=True)
    OUT.write_text(json.dumps(profiles, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Generated {len(profiles)} profiles → {OUT}")

    # Verify a few
    for sample_key in ["1-1", "2-3", "8-5", "13-1", "17-4"]:
        p = profiles.get(sample_key, {})
        kpi_titles = [k["title"] for k in p.get("kpis", [])]
        print(
            f"  {sample_key}: KPIs={kpi_titles}, headers={p.get('table_headers', [])[:3]}..."
        )


if __name__ == "__main__":
    main()
