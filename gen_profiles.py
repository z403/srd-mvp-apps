    # ── Chart titles ──
    subject = _extract_subject(challenge)
    chart_titles = {
        "monthly_trend": f"月次{subject}推移",
        "composition": f"{subject}種別構成",
        "comparison": f"部門別{subject}比較",
        "performance": f"{subject}管理指標",
    }

    # ── Other labels ──
    activity_section_title = f"{subject}の最新状況"
    progress_ring_labels = _generate_ring_labels(ftype)
